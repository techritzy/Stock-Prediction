user_str = input("Enter String : ")
chr_list = []
substring_list = []
len_substring = 0
substring = ""
for i in range(len(user_str)):
  if user_str[i] not in chr_list :
    chr_list.append(user_str[i])
    len_substring +=1
    substring = substring + user_str[i]
  else :
    substring_list.append(substring)
    chr_list = []
    len_substring = 0
    substring = ""

longest = 0
longest_sub = ""
for j in substring_list :
  if longest< len(j):
    longest = len(j)
    longest_sub = j

print(substring_list)
print("Longest substring in {} is {} with length {}".format(user_str,longest_sub,longest))